bool TryLoadKeyFromFile(const char* szFileName)
{
	if(szFileName==0 || szFileName[0]==0) return false;

	FILE* fp = fopen(szFileName, "rb");
	if(fp == 0) return false;

	fseek(fp, 0, SEEK_END);
	int nFileSize = ftell(fp);
	fseek(fp, 0, SEEK_SET);

	if(nFileSize != g_nPairKeySize+g_nPubKeySize)
	{
		fclose(fp); 
		return false;
	}

	fread(g_byPairKey, 1, g_nPairKeySize, fp);
	fread(g_byPubKey, 1, g_nPubKeySize, fp);
	fclose(fp);
	return true;
}